// Helper function to get color hex code from color name
export const getColorHex = (colorName) => {
    if (!colorName) return '#FFFFFF'; // Default to white if no color provided
    return colorMap[colorName.toLowerCase()] || colorName; // Return the hex code or the original if not found
};
